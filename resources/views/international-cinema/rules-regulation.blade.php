                        <h5>FIAPF Accredited Competitive, Feature Film Festival (20th- 28th November, 2023)</h5>
                        <p>Since 1952, India has hosted 53 International Film Festival of India (IFFI) both competitive and
                            non-competitive. The festival became an annual event from 1975 onwards. The 54th edition of the
                            festival which includes a competition for feature films by Directors from all Continents will be
                            held in Goa.</p>
                        <hr>
                        <h5>Festival Regulations</h5>
                        <p>The Festival is being organized by The National Film Development Corporation of India Ltd (NFDC),
                            Ministry of Information and Broadcasting, Government of India in collaboration with the State
                            Government of Goa and the Indian Film Industry. The Festival is recognized by the International
                            Federation of Film Producers Associations (FIAPF). 54th IFFI will have theatrical screenings of
                            selected films at different festival screens in Goa. It may also screen films on its official
                            digital platform during the festival in Goa, subject to the prevailing conditions.</p>
                        <hr>
                        <h5>1. Objectives :</h5>
                        <p> <strong>Vasudhaiva Kutumbakam</strong>(The Whole World is One Family) <strong>Celebrating the
                                Joy of
                                Cinema</strong> </p>
                        <p>The Festival aims at providing a common platform for film-makers of the world to project their
                            excellence in film art; contributing to the understanding and appreciation of film cultures of
                            different nations in the context of their social and cultural ethos; and promoting friendship
                            and
                            co-operation among people of the world.</p>
                        <hr>
                        <h5>2. Date and Place :</h5>
                        <p>The 54th edition of IFFI will be held from November 20th – 28th, 2023 in Goa, India.</p>
                        <hr>
                        <h5>3. Festival Programmes :</h5>
                        <p>The festival programme is broadly divided into the following categories: -</p>
                        <p>i) International Competition for Feature Films (up to 15 numbers. Up to three Indian Films will
                            be
                            selected for this section. Fiction Films with a minimum duration of 70 minutes and above).</p>
                        <p>ii) Non-Competition (World Panorama) - for feature films from around the world (Films with a
                            minimum
                            duration of 70 minutes and above).</p>
                        <p>iii) Country Focus & other specially curated packages (Indian & Foreign) and Special
                            Screenings/Gala
                            Premieres.</p>
                        <p>iv) Indian Panorama - for Indian feature films, Indian Panorama Regulations 2023 will be
                            applicable.
                        </p>
                        <p>v) Technical Workshops/Masterclasses/other Interactive & Academic Sessions/Panel
                            Discussions/Talent
                            Hub etc. both virtual and physical.</p>
                        <p>vi) Film Bazaar: IFFI also hosts the Film Bazaar, South Asia's largest and One of Asia's most
                            prolific Film Markets showcasing rich content to potential buyers from different countries
                            across
                            the world.</p>
                        <p>vii) National Film Development Corporation Ltd (NFDC) reserves the right to exclude films that
                            are
                            not in conformity with the philosophy and objective of the festival, which could offend the
                            feelings/susceptibilities of any participating country, or are likely to promote discrimination
                            of
                            any kind.</p>
                        <hr>
                        <h5>4. International Competition</h5>
                        <p>Films entered in the International Competition MUST fulfil the following conditions:</p>
                        <P>a) Films must be fiction, feature-length films should not be not less than 70 minutes in duration
                            and
                            in original language sound-track with English Subtitles.</P>
                        <p>b) They must have been completed between 1st September 2022 and 31st August 2023. Eligibility
                            will be
                            determined by the declaration made by the entrant in the film entry form. For International
                            films,
                            where no proof is available, the date of the first theatrical screening will be considered as
                            completion date/self-declaration by the applicant.</p>
                        <p>c) Except for Indian films, the entries in this section should not have been released/ shown on
                            virtual/ OTT platforms/ physically in India or presented in any other Indian Film Festivals
                            before.
                        </p>
                        <p>d) In the case of co-productions, the entrants must comply with article 15.3 of the Festival’s
                            regulations and therefore have the right to sanction the participation of the film in The
                            International Competition and also sanction the authority to receive awards, if the film wins
                            any.
                        </p>
                        <p>e) Entrants must agree that if their film/films win any of the Golden and/or Silver Peacock
                            Awards,
                            all screenings thereafter will acknowledge the Award(s) on the film itself.</p>
                        <p>f) Foreign Films for International Competition will be recommended by the Festival’s Preview
                            Committee. Upto three Indian film entries in International Competition will be identified by the
                            Indian Panorama Jury at the time of final selection of Indian Panorama Films for IFFI 2023. The
                            Festival Director holds the right to consider the films that are not accepted for the
                            International
                            Competition as entries for the ‘World Panorama'/Cinema of the World” Section. However, the
                            consent
                            of the entrant shall be sought before selection and inclusion in the festival programme.</p>
                        <p>g) The competition will be held subject to the acceptance of a maximum of fifteen eligible films
                            from
                            different Continents/Countries. If selected for the International Competition, the entrant whose
                            films would be awarded will be requested permission to provide the festival with a region-free
                            PAL
                            DVD/Blu-ray of the film for archival purposes.</p>
                        <p>h) The Jury for the International Competition will consist of a Chairman and not less than two
                            and
                            not more than four other members, of which one member will be from India.</p>
                        <p>i) The decisions of the jury will be taken by a simple majority of the members present and
                            voting.
                            The jury can evolve its own rules for the assessment of the entries. The Managing Director, NFDC
                            and/or his representative can attend the jury deliberations but shall neither participate nor
                            vote.
                        </p>
                        <p>j) No person having taken part in the production or having any commercial interest in a
                            competitive
                            entry may be on the jury. All members of the jury will refrain from releasing any articles or
                            reviews to the press concerning the film submitted for their judgment until the results of the
                            final
                            selection have been formally announced at the closing function.</p>
                        <p>k) Once the film has been selected and the right holder confirms her/his agreement, the film in
                            the
                            official programme shall not be withdrawn from the festival programme before or during the
                            event.
                        </p>
                        <p>l) The National Film Development Corporation Ltd may organize screenings of the IFFI
                            award-winning
                            films in various other festivals held under its responsibility. The right holders of IFFI
                            award-winning films will be requested permission for any screening of their film in such a
                            framework.</P>
                        <p>m) The following prizes will be given away:</p>
                        <p><strong><span style="font-size:18px">(I) Best Film</span></strong></p>
                        <p>a) A Cash prize of Rs. 40,00,000/- to be shared equally between the Director and Producer.</p>
                        <p>b) Director is to be given the Golden Peacock and a Certificate in addition to the cash
                            component.
                        </p>
                        <p>c) Producer to be given a Certificate in addition to the cash component.</p>
                        <p><strong><span style="font-size:18px">(II) Best Director</span></strong></p>
                        <p>Silver Peacock, Certificate and a cash prize of Rs.15,00,000/-</p>
                        <p><strong><span style="font-size:18px">(III) Best Actor (Male)</span></strong></p>
                        <p>Silver Peacock, Certificate and a cash prize of Rs. 10,00,000/-</p>
                        <p><strong><span style="font-size:18px">(IV)Best Actor (Female)</span></strong></p>
                        <p>Silver Peacock, Certificate and a cash prize of Rs. 10,00,000/-</p>
                        <p><strong><span style="font-size:18px">(V)Special Jury Award</span></strong></p>
                        <p>Silver Peacock, Certificate and a cash prize of Rs. 15,00,000/- given to a film (for any aspect
                            of
                            the film which the jury wishes to award/acknowledge) or an individual (for his/her artistic
                            contribution to a film). The award, if given to a film, will be given to the Director of the
                            film.
                        </p>
                        <p><strong><span style="font-size:18px">(VI) Award for Best Debut Feature Film of a
                                    Director</span></strong></p>
                        <p>a) Silver Peacock, certificate and a cash prize of Rs. 10,00,000/- (To be given to a director for
                            his/her best first feature film to recognize and encourage the most promising new directorial
                            talent
                            in world cinema).</p>
                        <p>b) Out of the entries received for this category of award, up to five non-Indian (foreign) films
                            will
                            be shortlisted by the Preview Committee. The festival holds the right to consider the films that
                            are
                            not shortlisted for this category as entries for the World Panorama Section of the Festivals.
                        </p>
                        <p>c) First feature film of a director selected under Indian Panorama is also eligible for this
                            Award.
                            Indian film entry for the Award for the Best Debut Feature Film of a Director will be identified
                            by
                            the Indian Panorama Jury from the selected films for Indian Panorama 2023. A maximum of two such
                            films can be recommended by the Indian Panorama Jury at the time of the final selection of
                            Indian
                            Panorama Films for IFFI 2023.</p>
                        <p>d) Films entered for Award for the Best Debut Feature Film of a Director MUST fulfil the
                            following
                            conditions: -</p>
                        <p>i) Films must be first fiction, feature-length films, not less than 70 minutes in duration in
                            original language sound-track with English Subtitles. Films in English need not be subtitled.
                        </p>
                        <p>ii) They must have been completed between 1st September 2022 to 31st August 2023 (For Indian
                            Feature
                            Films, dates will be applicable as per Indian Panorama Regulations 2023). Eligibility will be
                            determined by the declaration made by the entrant in the film entry form. For International
                            films
                            where no proof is available, the date of the first theatrical screening will be considered the
                            completion date.</p>
                        <p>iii) Except for Indian films, the entries in this section should not have been released/ shown in
                            India or presented in any other Indian Film Festivals before.</p>
                        <p>iv) In the case of co-productions, the entrants must comply with article 15.3 of the Festival’s
                            regulations and therefore have the right to sanction participation of the film for this award
                            and
                            also sanction the authority to receive awards, if the film wins any.</p>
                        <p>v) Entrants must agree that if their film/films win the Award, all screenings thereafter will
                            acknowledge the Award on the film itself.</p>
                        <p>vi) If shortlisted for competing for this award, the entrant(s) - must provide a DCP (Non-
                            encrypted,
                            DCI Compliant-J2K Intrope or SMPTE in 24 fps) with EST for festival screenings - must agree that
                            in
                            case of winning an award, they will provide the festival with a region free PAL DVD/Blu-ray copy
                            of
                            the film for archival purposes.</p>
                        <p>vii) The winner of the award will be recommended by the International Jury of IFFI 2023 to the
                            Ministry of Information and Broadcasting, Government of India.</p>
                        <p><strong><span style="font-size:18px">(VII) Satyajit Ray Lifetime Achievement Award for Excellence
                                    in
                                    Cinema</span></strong></p>
                        <p>This prestigious award, instituted in the name of the legendry filmmaker the Satyajit Ray,
                            consisting
                            of a cash prize of Rs. 10,00,000/-, certificate, shawl, Medal (Silver Peacock) and a scroll, is
                            conferred upon a master film-maker/film technician/film personalities for his/her outstanding
                            contribution to cinema. The festival every year invites a film maker/ film technician/ film
                            personalities of repute for this award.</p>
                        <p><strong><span style="font-size:18px">(VIII) Indian Film Personality of the Year
                                    Award</span></strong>
                        </p>
                        <p>This prestigious award consisting of a cash prize of Rs. 10,00,000/-, certificate, shawl and
                            Medal
                            (Silver Peacock) is conferred upon an Indian film personality for his/her outstanding
                            contribution
                            to Indian Cinema.</p>
                        <p><strong><span style="font-size:18px">(IX) ICFT UNESCO – Gandhi Medal Award</span></strong></p>
                        <p>IFFI 2023 in collaboration with the International Council for Film, Television and Audiovisual
                            Communication (ICFT), Paris, will present a special ICFT prize consisting of the UNESCO Gandhi
                            Medal
                            awarded to a film which reflects the ideals promoted by UNESCO - especially: Tolerance,
                            Intercultural Dialogue & Culture of Peace.</p>
                        <p>Broad guidelines for the selection of the films for the UNESCO Gandhi Medal:</p>
                        <p>a) Have a strong ethical component.</p>
                        <p>b) High aesthetic value.</p>
                        <p>c) Exhibit artistic excellence in screenplay, film technique and music.</p>
                        <p>d) Attract a wide audience, especially among youth, whose view of media is distorted by violent
                            imagery and showy techniques which are devoid of intelligence and critical discernment.</p>
                        <p>e) Additionally include an educational component to cope with the flood of inappropriate imagery
                            commonly marketed.</p>
                        <p>f) Awareness of the role of the filmmaker in confronting a declining society; because it is
                            recognized that cinematographic and video images are much more powerful than any other means of
                            communication.</p>
                        <p>IFFI Preview Committee shall recommend upto 7 films for this award’s competition from the entries
                            received in the International Cinema section. While up to 3 Indian films for this Award’s
                            competition will be shortlisted by Indian Panorama Jury from the entries in the Indian Panorama
                            Section.</p>
                        <p>The Gandhi Medal Award Jury members nominated by ICFT UNESCO shall recommend this award from the
                            films entered in this competition. The Award consists of a Certificate and a medal given by ICFT
                            UNESCO.</p>
                        <p>Recipients are to agree that the concerned professional will receive the award in person, if the
                            film/he/she wins any award. National Film Development Corporation Ltd reserves the right to
                            cancel
                            the cash component in case of the awardees not being able to present him/her at the award
                            ceremony
                            on the closing night. For any reason the awardee is not able to personally attend, he or she
                            should
                            immediately in writing, nominate an immediate family member or from the film-its Producer or
                            Director.</p>
                        <hr>
                        <h5>5. Non- Competition (World Panorama)</h5>
                        <p>a) Films must be feature-length films not less than 70 minutes in duration in original language
                            soundtrack with English Subtitles. Films in English need not be subtitled.</p>
                        <p>b) They must have been completed after 1st September 2022.</p>
                        <p>c) If selected for the World Panorama Section, the entrant(s) must provide a 35 mm print or a DCP
                            with EST and optical sound-track or originally shot in a digital format for festival screenings.
                        </p>
                        <p>d) Films that have been presented in earlier editions of the International Film Festival of
                            India, or
                            already screened in India, will be excluded, unless previously agreed to by the Managing
                            Director
                            NFDC, in exceptional cases.</p>
                        <hr>
                        <h5>6. Entry Deadlines</h5>
                        <p>The deadline for receiving applications and preview material is 31st August 2023.</p>
                        <h5>7. DVD Screeners for Selection</h5>
                        <p>The Online/ Blu-ray /DVD screeners with English subtitles should reach the National Film
                            Development
                            Corporation Ltd, New Delhi office before the above-mentioned date along with a duly completed
                            online
                            Entry Form (available on our website). Inscribe 'No commercial value for Cultural purposes only'
                            on
                            the parcel. The Festival will not bear the cost of transportation of preview material, unless
                            upon
                            the entrant’s request and at his/her own expense.</p>
                        <hr>
                        <h5>8. Entering films in the festival</h5>
                        <p>Film producers, distributors, their duly authorized representatives or duly authorized Government
                            organizations/agencies of the participating countries may enter their films for the
                            International
                            Competition, World Panorama and Country Focus Sections in the Entry Form.</p>
                        <p>a) Indian Films may submit their applications through the Indian Panorama, which will be opened
                            and
                            advertised in all the national dailies. The same will be available on the official website of
                            the
                            festival (https://www.iffigoa.org or https://nfdcindia.com) as well.</p>
                        <p>b) Enquiries for Film Bazaar may be addressed to the National Film Development Corporation
                            (NFDC).
                            The contact address: Director, <br>
                            Film Bazaar National Film Development Corporation Discovery of India Building, <br>
                            Nehru Centre, Dr Annie Besant Road, Worli Mumbai- 400 018- India. <br>
                            Ph:<a href="tel:02266288295">022-66288295 </a>, Fax: <a href="tel:02224952262">022-24952262</a>
                            <br>
                            Email: <a href="mailto:director@filmbazaarindia.com">director@filmbazaarindia.com</a>
                        </p>
                        <p>c) National Film Development Corporation Ltd may also invite films which it considers of
                            interest.
                        </p>
                        <p>d) The Form, duly filled in, separately for each film proposed to be entered, should reach the
                            National Film Development Corporation Ltd by August 31st, 2023 through the online entry route at
                            www.iffigoa.org. In case of the day being a holiday, the next working day will be considered as
                            the
                            deadline.</p>
                        <hr>
                        <h5>9. Final Screening Materials</h5>
                        <p>a) 35mm prints/DCPs/final screening material along with a Blu-ray/DVD of the selected films for
                            exhibition in the festival must reach the festival Office in Goa on or before 15th October 2023.
                            The
                            Festival prefers online transfer of DCPs with KDMs for the entire festival period. If sent
                            through
                            physical mode, prints should only be sent through the official freight handlers of the festival,
                            as
                            per the instructions given by the festival print unit. National Film Development Corporation Ltd
                            will not be responsible for any repercussions arising from deviation of the print dispatch
                            instructions issued by the print unit.</p>
                        <p>b) After the conclusion of the festival, the final screening materials if received in physical
                            form,
                            shall be returned through air- freight or diplomatic channels, as per specific written
                            instructions
                            received from the film right holder. The festival would bear only one-way freight. Wherever
                            previously agreed upon by the festival, the festival will also be undertaking the actual cost of
                            return freight charges (both ways) but will not be liable for payment of customs, duties,
                            freight
                            handler's fees etc., outside India. It will be borne solely by the film recipient.</p>
                        <hr>
                        <h5>10. Insurance</h5>
                        <p>The Insurance of prints in transit from the country of origin to the National Film Development
                            Corporation Ltd and from the National Film Development Corporation Ltd to the country of origin
                            will
                            be the responsibility of the entrant. The National Film Development Corporation Ltd will insure
                            the
                            prints against damage, partial and total, by fire, theft or any other unnatural causes from the
                            moment it takes delivery of the prints to the moment it hands them back to the concerned
                            airline/agency for dispatch, in conformity with Addendum 1 of the FIAPF Regulations for
                            International Film Festivals. Technical complaints regarding the returned screening copy must be
                            reported to the festival in writing within 5 days of being received.</p>
                        <hr>
                        <h5>11. Freight and Other Charges</h5>
                        <p>a) The prints and DVD's should be sent "freight pre-paid" i.e. at the sender's cost. After the
                            Festival, the prints will be returned to the senders by air freight at the cost of the National
                            Film
                            Development Corporation Ltd.</p>
                        <p>b) The National Film Development Corporation Ltd will not pay any charges other than the
                            air-freight
                            charges. It may also be noted that the National Film Development Corporation Ltd cannot
                            reimburse
                            any freight or other charges in foreign currency, unless previously agreed to in writing.</p>
                        <p>c) The National Film Development Corporation Ltd will not bear freight or any other charges for
                            unsolicited films, Videos or DVDs sent to it if these are not selected for screening in the
                            Festival.</p>
                        <hr>
                        <h5>12. Subtitling</h5>
                        <p>Films will only be screened in their original language/version with English Subtitles. Non
                            English
                            films without English subtitles will not be screened at the festival and they will also not be
                            electronically subtitled by the Festival.</p>
                        <hr>
                        <h5>13. Screening</h5>
                        <p>a) The films being screened at IFFI would either be certified by the Central Board of Film
                            Certification, India or have obtained an exemption under Section 9 of the Cinematograph Act,
                            1952.
                        </p>
                        <p>b) The accepted film will not be shown outside the Festival city by the National Film Development
                            Corporation Ltd, without written consent of the right holder.</p>
                        <p>c) The Festival proposes to have a maximum of five screenings per selected film as per FIAPF
                            Regulations for International Film festivals and for the competitive specialized festivals which
                            allow for a maximum of five screenings per film.</p>
                        <hr>
                        <h5>14. Excerpts for Television</h5>
                        <p>For the promotion of films entered in the Festival on television, producers/authorized entrants
                            are
                            requested to supply four excerpts of the film(s) entered, each of a maximum of three minutes, to
                            the
                            National Film Development Corporation Ltd. High-Resolution uncompressed video of the same may be
                            sent in a DVD format.</p>
                        <hr>
                        <h5>15. General</h5>
                        <p>a) Classroom and advertising films are not eligible for participation in any section.</p>
                        <p>b) Final screening material must comply with technical standards specified by SMPTE for screening
                            at
                            International Film Festivals. National Film Development Corporation Ltd reserves the right to
                            refuse
                            screening if found otherwise.</p>
                        <p>c) The participants must ensure that they have the right to participate in the International Film
                            Festival of India with regards to parties connected with the production /distribution of the
                            participating film and /or other right holders/licenses.</p>
                        <p>d) Participation in the Festival implies acceptance of these Regulations.</p>
                        <p>e) The Managing Director of National Film Development Corporation Ltd will decide all matters not
                            expressly provided for in these regulations but in conformity with the provisions of the
                            International Regulations for film festivals and his decisions shall be final and binding on all
                            Parties.</p>
