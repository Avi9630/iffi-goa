                        <h5>13. Screening</h5>
                        <p>a) For screening at IFFI, all films need to either have been certified by the Central Board of
                            Film Certification, India or need to have obtained exemption under Section 9 of Cinematograph
                            Act, 1952. The Festival will undertake formalities on behalf of all uncertified selected films
                            for exemption from the Ministry of Information & Broadcasting. Certified films or filmsin
                            process of certification must complete their certification and submit the certificate to the
                            Festival prior to its commencement.
                        </p>
                        <p>b) The accepted films will not be shown outside the Festival zone by the National Film
                            Development Corporation Ltd, without written consent of the right holder.</p>

                        <p>c) The Festival proposes to have a maximum of five screenings per selected films as per FIAPF
                            Regulations for International Film festivals and for the competitive specialized festivals which
                            allow for a maximum of five screenings per film.</p>

