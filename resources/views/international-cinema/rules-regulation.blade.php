                        <h5>5. Non- Competition (Cinema of the World & Curated Packages)</h5>

                        <p>a) Films for inclusion in this section, must be feature length, not lessthan 72 minutesin
                            duration, in original language soundtrack with English Subtitles. English Language films should
                            also preferably carry subtitles in English. For its curated packages, the Festival may relax the
                            eligibility criteria of duration, subject to the merit of the film and the curatorial discretion
                            for the segment. Fiction and documentary 6 features along with other forms of feature genres and
                            aesthetics are eligible. </p>

                        <p>b) They must have been completed after 1st September 2024.</p>

                        <p>c) If selected for the World Panorama Section, the entrant(s) must provide a 35 mm print or a DCP
                            with EST.
                        </p>

                        <p>d) Films that have been presented in earlier editions of the International Film Festival of
                            India, or already screened in India, will be excluded, unless previously agreed to by the
                            Managing
                            Director NFDC, in exceptional cases.
                        </p>

                        <p>e) Non-competition and Curated Packages are subject to the artistic vision of the Festival and
                            will be determined by the Artistic Director with inputs from the International Programming
                            Department for international films, and Indian Panorama Department for Indian films.</p>

