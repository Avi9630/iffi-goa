                        <h5>1. Short Title</h5>
                        <p></p>
                        <p>
                            The International Film Festival of India (IFFI), is a competitive feature film festival
                            accredited by the International
                            Federation of Film Producers Associations (FIAPF). Since its inception, IFFI has consistently
                            proven to be a film
                            festival par excellence, celebrating the finest cinematic art and talents from around the globe.
                            This year, IFFI will also
                            focus on Indian debut films, which will showcase a diverse range of narratives and cinematic
                            styles of young
                            filmmakers from across the country. These regulations shall be called Best Debut Indian Film
                            Regulations for the 55th
                            edition of International Film Festival India (IFFI), 2024 and shall come into force with
                            immediate effect.
                        </p>
                        <p></p>
                        <hr>
                        <h5>2. Definitions</h5>
                        <p></p>
                        <p>In these Regulations, unless the context otherwise refers,</p>
                        <ul class="ps-0">
                            <li class="liststyleNone">(a) “Board” means the Central Board of Film Certification.</li>
                            <li class="liststyleNone">(b) “Corporation” means National Film Development Corporation,
                                Ministry of Information and Broadcasting.</li>
                            <li class="liststyleNone">(c) "Producer" is one who produces a film and also has the copyright
                                of the film and can supply/authorize supply
                                of digital copies/cinematographic prints from the laboratory for Jury or other screenings at
                                Film Festival(s) of the
                                selected films and may include an individual or a company or an Institute or any other such
                                entity and shall include a
                                co- producer also.</li>
                            <li class="liststyleNone">(d) "Eligibility Period" refers to the period from 1st August 2023 to
                                31st July 2024.</li>
                            <li class="liststyleNone">(e) "Entry" refers to the film for which the application for the award
                                is made.</li>
                        </ul>
                        <p></p>

                        <hr>

                        <h5>3. Aims and objectives</h5>
                        <p></p>
                        <ul class="ps-0">
                            <li class="liststyleNone">(a) <b>To promote new and young talent</b> by identifying and honoring
                                debut
                                films by Indian filmmakers, thereby,
                                encouraging the growth of fresh talent in the film industry.</li>
                            <li class="liststyleNone">(b) <b>To encourage new perspectives and creativity</b> by recognizing
                                and rewarding films that push the boundaries
                                of traditional storytelling and filmmaking, demonstrating originality and creative vision.
                            </li>
                            <li class="liststyleNone">(c) <b>To foster global appreciation</b> by providing a platform for
                                debut
                                films to reach a wider audience, both
                                nationally and internationally.</li>
                        </ul>
                        <p></p>

                        <hr>

