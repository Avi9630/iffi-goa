<h6>5. ELIGIBILITY CRITERIA</h6>
<p>All entries must fulfil the following conditions:</p>
<ul class="ps-0">
    <li>5.1 The web series should be originally created/shot and available in any Indian language(s).</li>
    <li>5.2 The web series should be an original piece of work commissioned, produced, co-produced, licensed, or acquired with the purpose of releasing only on the OTT Platform.</li>
    <li>5.3 The following conditions shall render entries ineligible for participation:
        <ul class="ps-0">
            <li>5.3.1 If the web series was originally released in theatres or on television.</li>
            <li>5.3.2 A dubbed, revised, remake, re-edited version of an existing web series.</li>
            <li>5.3.3 Previews, trailers, sizzle reels, commercials, reality shows, etc.</li>
            <li>5.3.4 Collection of sequences from feature-length films, aired and unaired episodes of an established TV show previously or simultaneously released theatrically or aired on television.</li>
        </ul>
    </li>
    <li>5.4 The web series should have been originally released on one or more OTT platform(s)</li>
    <li>5.5 In case of web series having multiple seasons, the entry can be made only for one season as per the choice of the applicant, subject to other eligibility criteria.</li>
    <li>5.6 All episodes of the entry (web series/season) should be available for public viewing on an OTT platform.</li>
    <li>5.7 All episodes of the entry (web series/season), should have been released on an OTT Platform during the eligibility period i.e. from 1st August, 2024 to 31st July, 2025.
        <ul class="ps-0">
            <li>5.7.1 In case the web series is released on multiple OTT Platforms, the platform on which the web series was originally streamed will be considered.</li>
            <li>5.7.2 In case any episode(s) of a web series cuts across the eligibility period, the web series shall be eligible to be considered for the next edition.</li>
        </ul>
    </li>
    <li>5.8 The web series/season under application must:
        <ul class="ps-0">
            <li>5.8.1 have a total runtime of at least 180 minutes</li>
            <li>5.8.2 have a minimum of three (3) or more episodes</li>
            <li>5.8.3 have each episode of 25 mins or more in duration</li>
            <li>5.8.4 be tied together under the same title or trade name.</li>
        </ul>
    </li>
    <li>5.9 Regulatory Aspects related to IT Rules, 2021:
        <ul class="ps-0">
            <li>5.9.1 The OTT platform must have furnished information to the Ministry of Information Broadcasting in accordance with Rule 18 of the IT Rules, 2021, and should be in compliance with Part III of the IT Rules, 2021.</li>
            <li>5.9.2 The web series should be in compliance with the Code of Ethics for online curated content, prescribed in the IT Rules, 2021.</li>
            <li>5.9.3 The web series should not have any adverse order regarding its content under the IT Rules, 2021, or any court of law in India.</li>
        </ul>
    </li>
    <li>5.10 Disqualification:
        <ul class="ps-0">
            <li>5.10.1 An applicant will be automatically disqualified from consideration if it is discovered that they are attempting to influence any member of the Jury. The decision of the Secretary, Ministry in this matter will be final and binding.</li>
            <li>5.10.2 If any declaration submitted by the applicant is found to be false or incorrect at any stage, the Ministry of Information Broadcasting reserves the right to reject the entry.</li>
        </ul>
    </li>
</ul>

