    <li>6.7 If the applicant fails to comply with any of the requirements mentioned in this section of the Regulations, the entry may be summarily rejected.</li>
</ul>

<h6>7. SELECTION PROCESS</h6>
<p>There shall be a two-tier system i.e. the Preview Committee and the Jury, for making selections for the award as specified in Clause 4 of the Regulations. The Ministry will duly constitute both the Preview Committee and the Jury to carry out the selection process.</p>
<ul class="ps-0">
    <li>7.1 Preview Committee
        <ul class="ps-0">
            <li>7.1.1 The preview committee will be comprised of members distinguished in the field of web-series, cinema, and other allied arts from across India.</li>
            <li>7.1.2 The preview committee will shortlist and recommend up to ten (10) web series from the eligible applications to the Jury for the final selection of the award, as specified in Clause 4 of the Regulations.</li>
        </ul>
    </li>
    <li>7.2 Jury
        <ul class="ps-0">
            <li>7.2.1 A Jury will be comprising of eminent film/web series professionals/ personalities acclaimed in the field of web-series, cinema, and other allied arts from across India.</li>
            <li>7.2.2 The Jury will select up to five (5) nominations from the shortlisted web series, out of which they will choose ONE as the winner.</li>
            <li>7.2.3 The Jury will determine their own work procedure. The Jury would be the apex decision-making body for the category of award as specified in the Clause 4 of the Regulations.</li>
            <li>7.2.4 A person will be ineligible to serve on Preview Committee/ Jury if a web-series in which he/she is directly associated (professionally, financially or otherwise) is an entry. In the event of his / her close relative being associated with the particular entry, the Preview Committee /Jury member shall recuse himself/herself from the selection process of the web series.</li>
            <li>7.2.5 The members appointed for the preview committee, Jury, and Chairperson shall make a declaration, in the prescribed format of SCHEDULE II of these Regulations.</li>
            <li>7.2.6 The preview committee and Jury members shall maintain strict confidentiality regarding the deliberations/recommendations of the web-series during the selection process. In case of any violation by the preview committee and Jury members, he/she would be debarred from becoming a Jury member in any awards hosted by the Ministry.</li>
            <li>7.2.7 The Festival Director, IFFI, and/or his/her nominee may attend the deliberations of the panels and provide information/clarifications, required by the panels regarding the entries. They shall however neither participate in the deliberations nor vote.</li>
            <li>7.2.8 The decision of the Jury shall be final and binding and no appeal or correspondence regarding their decision shall be entertained.</li>
        </ul>
    </li>
</ul>

<h6>8. DISPUTE RESOLUTION</h6>
<p>Should any dispute or difference arise, the same shall be referred to the Secretary, Ministry, whose decision in the matter shall be final and binding. The Arbitration Clause will not apply to the decision of the Jury, which will be final and binding.</p>


