<h5>BEST WEB SERIES (OTT) AWARD</h5>

<h6>1. SHORT TITLE</h6>
<p>
    The International Film Festival of India (IFFI), is competitive feature film festival accredited by the International Federation of Film Producers Associations (FIAPF). Since its inception, IFFI has consistently proven to be a film festival par excellence, celebrating the finest cinematic art and talents from around the globe. Recognising the tectonic changes in the entertainment industry especially in the recent years with the onset and acceptance of Over-The-top (OTT) platforms, the Best Web Series (OTT) Award was introduced starting with the 54th edition of IFFI (2023). Continuing the momentum, the award aims at acknowledging, encouraging and honouring the flourishing content and its creators on OTT platforms.
</p>

<h6>2. DEFINITIONS</h6>
<ul class="ps-0">
    <li>‘Applicant’ refers to:
        <ul class="ps-0">
            <li>● Producer(s) normally working and residing in India</li>
            <li>● Production house(s) incorporated/registered in India</li>
            <li>● An OTT platform in the case of its original production or co-production content. The OTT platform should be compliant with Part III of the IT Rules 2021</li>
            <li>● In the case of a co-production between any of the above, the majority rights holder.</li>
        </ul>
    </li>
    <li>‘Eligibility Period’ refers to the period from 1st August, 2024 to 31st July, 2025.</li>
    <li>‘Entry’ refers to the web series or its particular season (in case of web series with multiple seasons), for which the application for the award is made.</li>
    <li>‘IT Rules, 2021’ refers to The Information Technology (Intermediary Guidelines and Digital Media Ethics Code) Rules, 2021</li>
    <li>‘Ministry’ refers to the Ministry of Information & Broadcasting, Government of India</li>
    <li>‘OTT Platform’ refers to a publisher of online curated content as defined under IT Rules, 2021</li>
    <li>‘Web Series’ refers to a form of episodic content purposed to be streamed and watched over the internet through various Over-the-top (OTT) platforms. It consists of a series of interconnected episodes and/ or seasons, following a narrative storyline, central idea, or thematic concept covering a variety of genres.</li>
</ul>

<h6>3. OBJECTIVES OF THE BEST WEB SERIES (OTT) AWARD</h6>
<ul class="ps-0">
    <li>● To foster growth and innovation in the Indian OTT industry by encouraging and celebrating web series content produced for and screened on digital platforms.</li>
    <li>● To encourage OTT content in Indian languages by promoting regional diversity and creativity in the web content industry, including content produced in regional languages.</li>
    <li>● To recognize and reward exceptional talent that has realized its potential due to the opportunities provided by the OTT space in India.</li>
    <li>● To incentivize and create investment opportunities in India's OTT sector by providing avenues to content creators and OTT platforms to showcase their work, exchange knowledge, and foster international relationships through the International Film Festival of India (IFFI).</li>
</ul>

